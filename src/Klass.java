public class Klass {
        private int number;
        private String word;
        private int[] array;

        public Klass(int number, String word, int[] array){
            this.number = number;
            this.word = word;
            this.array = array;
        }
        public int getNumber() {
            return number;
        }
        public int[] getArray() {
            return array;
        }

        public String getWord() {
            return word;
        }


    }

